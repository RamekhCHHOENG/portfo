<script setup lang="ts">
const { downloadPDF, isGenerating } = usePdfDownload()

const form = reactive({ name: '', email: '', subject: '', message: '' })
const errors = reactive({ name: '', email: '', subject: '', message: '' })
const submitting = ref(false)
const submitted = ref(false)
const sent = ref(false)
const submitError = ref('')

function validate() {
  let valid = true
  errors.name = form.name.trim() ? '' : 'Name is required.'
  errors.subject = form.subject.trim() ? '' : 'Subject is required.'
  errors.message = form.message.trim() ? '' : 'Message is required.'

  if (!form.email.trim()) {
    errors.email = 'Email is required.'
  } else if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(form.email)) {
    errors.email = 'Enter a valid email address.'
  } else {
    errors.email = ''
  }

  valid = !errors.name && !errors.email && !errors.subject && !errors.message
  return valid
}

async function handleSubmit() {
  submitError.value = ''
  if (!validate()) return

  submitting.value = true
  try {
    // Opens the user's email client pre-filled with the message.
    // Replace with a server endpoint or service like Formspree for server-side sending.
    const body = encodeURIComponent(
      `Name: ${form.name}\nEmail: ${form.email}\n\n${form.message}`
    )
    const subject = encodeURIComponent(form.subject)
    window.location.href = `mailto:ramekhchhoeng@icloud.com?subject=${subject}&body=${body}`
    sent.value = true
  } catch {
    submitError.value = 'Something went wrong. Please try emailing me directly.'
  } finally {
    submitting.value = false
  }
}

function resetForm() {
  form.name = ''
  form.email = ''
  form.subject = ''
  form.message = ''
  errors.name = ''
  errors.email = ''
  errors.subject = ''
  errors.message = ''
  sent.value = false
  submitError.value = ''
}
</script>
